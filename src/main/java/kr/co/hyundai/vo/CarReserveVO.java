package kr.co.hyundai.vo;

public class CarReserveVO {

	private int reserveNo;
	private String id;
	private int no;
	private int qty;
	private int dday;
	private String rday;
	private int insurance;
	private int wifi;
	private int babyseat;
	private int navination;
	
	//getter&setter
	public int getReserveNo() {
		return reserveNo;
	}
	public void setReserveNo(int reserveNo) {
		this.reserveNo = reserveNo;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public int getDday() {
		return dday;
	}
	public void setDday(int dday) {
		this.dday = dday;
	}
	public String getRday() {
		return rday;
	}
	public void setRday(String rday) {
		this.rday = rday;
	}
	public int getInsurance() {
		return insurance;
	}
	public void setInsurance(int insurance) {
		this.insurance = insurance;
	}
	public int getWifi() {
		return wifi;
	}
	public void setWifi(int wifi) {
		this.wifi = wifi;
	}
	public int getBabyseat() {
		return babyseat;
	}
	public void setBabyseat(int babyseat) {
		this.babyseat = babyseat;
	}
	public int getNavination() {
		return navination;
	}
	public void setNavination(int navination) {
		this.navination = navination;
	}
	
	
	
	
}
